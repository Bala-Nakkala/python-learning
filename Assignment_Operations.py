# Assignment Operators in Python

# Simple Assignment
x = 10
print(x)  # Output: 10

# addition assignment
x += 5
print(x) # output: 15

# substraction assignment
x -= 3
print(x) # output: 12

# multiplication assignment
x *= 2
print(x) # output: 24

# division assignment
x /= 4
print(x) # output: 6.0  

# modulus assignment
x %= 4
print(x) # output: 2.0

# floor division assignment
x //= 2
print(x) # output: 1.0  

# exponentiation assignment
x **= 3
print(x) # output: 1.0



