# Relational Operations in Python
a = 5
b = 10

# equal to
result = a == b
print(result)  # Output: False

# not equal to
result = a != b
print(result)  # Output: True

# less than
result = a < b
print(result)  # Output: True

# greater than
result = a > b
print(result)  # Output: False

# less than or equal to
result = a <= b
print(result)  # Output: True

# greater than or equal to
result = a >= b
print(result)  # Output: False

